public class ArrayNama {
    public static void main(String [] Args) {
        String namaString[] = new String[5];
        namaString[0] = "Linda";      
        namaString[1] = "Santi";       
        namaString[2] = "Susan";        
        namaString[3] = "Mila";        
        namaString[4] = "Ayu";
        for (int i = 0;i<namaString.length;i++){
            System.out.println(namaString[i]);
        }        
    }
}
